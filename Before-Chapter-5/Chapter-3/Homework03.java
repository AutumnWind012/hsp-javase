public class Homework03 {
	public static void main(String[] args) {
		// 书名用 String
		String book1 = "天龙八部";
		String book2 = "笑傲江湖";
		// 字符串拼接
		System.out.println(book1 + " " + book2);
		
		// 性别用 char
		char a1 = '男';
		char a2 = '女';
		// 字符的本质是整数（编码相加）
		System.out.println(a1 + a2);
		
		// 价格用 double
		double p1 = 123.56;
		double p2 = 100.11;
		// 223.4
		System.out.println(p1 + p2);

	}
}